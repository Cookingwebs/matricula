y del Consejo , de 27 de abril de 2016 , relativo a la protección de las personas físicas en lo que respecta al
tratamiento de datos personales y a la libre circulación de estos datos (RGPD) , así como la Ley Orgánica 3/2018 ,
de 5 de diciembre , de Protección de Datos Personales y garantía de los derechos digitales (LOPD).</p>
