<h3 style="margin-top:100px">MATRICULACIÓN DEL ALUMNO Y RESERVA DE PLAZA</h3>

	<ul>
			<li>Una vez que se efectúe la cumplimentación de los datos que figuran en la matrícula, con ello el
		alumno/empresa declara que es conocedor de la actividad académica que imparte AYUDA T
		LEARNINGy muestra su interés en realizar el programa en el que se matricula, y que conoce de forma
		clara y precisa, todo el contenido, características del mismo, así como los horarios de estudio, su
		precio, modalidad de pago y exigencias.</li>
		 <li>En el momento en que el alumno/empresa cumplimenta la matrícula, y haga efectivo el pago en la
		forma que se establece en “Formas de Pago” AYUDA T LEARNING Se obliga a asignar una plaza en el
		curso matriculado, para poder recibir el programa que se indica en la matrícula y el material indicado
		para que pueda superar la acción formativa.</li>
		 <li>AYUDA T LEARNING Se reserva el derecho a proporcionarle al alumno las claves de acceso hasta el
		inicio del programa en el que se haya matriculado.</li>
	</ul>
	<h3>COMPROMISOS DEL ALUMNO</h3>
	<ul>
		<li>El alumno adquiere el compromiso de desarrollar el programa con aprovechamiento y a obedecer y
respetar la disciplina académica de AYUDA T PYMES. Es requisito de los programas, realizar al menos,
las actividades evaluables que se proponen a lo largo del desarrollo de este.</li>
<li>El alumno manifiesta que tienen los conocimientos informáticos necesarios que le permiten continuar
la metodología del presente programa, y a su vez, desarrollarlo con todas lasgarantías para adquirir los
conocimientos necesarios para obtener el título correspondiente.</li>
<li>Bajo ningún concepto, el alumno/empresa podrá utilizar con fines distintos a los de su propia
formación la documentación, notas técnicas, casos prácticos, actividades, etc, que AYUDA T
LEARNING le facilite o le pueda facilitar, salvo que conste una autorización por escrito de la misma.
<li>El alumno/empresa no queda autorizado a realizar ninguna de las acciones que se enumeran a
continuación: distribuir, transmitir, copiar, mostrar, ejecutar, reproducir, conceder licencias, usar el
material como base u apoyo para otros trabajos, ni transferir ni vender bajo ninguna forma el tipo de
formación, documentos, documentación, imágenes, código de programación, logotipos, Marcas,
productos o servicios obtenidos de sus marcas.</li>
<li>Para que el alumno pueda completar de forma correcta el funcionamiento del programa, este declara
poseer o tener acceso a un equipo informático adecuado para las especificaciones del programa, bien
sea propio o ajeno, y una conexión a internet con el fin de poder seguir el programa on- line en el que
se está matriculando, como complemento a su formación, para el supuesto de que así se requiera. Y a
mantenerlo durante todo el tiempo que dure el programa.</li>
<li>El alumno tendrá acceso al Campus Virtual mediante un nombre de usuario y una clave personal de
acceso o Password. La utilización de esta clave es totalmente personal e instransferible. Para el
supuesto de sufrir pérdida o robo de la clave de usuario, deberá comunicarlo de forma inmediata a
AYUDA T PYMES. En caso de que no lo comunique, será Usted el responsable de las modificaciones y
utilizaciones que se apliquen en su cuenta, hasta ese momento.</li>
	</ul>
	<p>Webinars semanales: se realizarán en el horario propuesto por el profesor en función de la convocatoria, podrá ser por la mañana en la franja horaria de 8h a 15h o por la tarde de 16h a 18h. Grabaremos las sesiones, por lo que los alumnos que no estén disponibles a la hora propuesta de la webinar, la podrán ver luego en diferido.</p>
	<p>Titulación: La titulación del curso se enviará por e mail al alumno a partir del día de finalización que se
establece en el correo de Bienvenida, así como en el Campus Virtual del usuario. Para el caso de que la
acción formativa se finalizara antes, entonces podrá solicitar el diploma antes del día de la finalización
establecido, resultando preciso que envíe la solicitud por escrito a
administracion@ayudatlearning.com. Con las especificaciones siguientes: Nombre y Apellidos, DNI, y
curso realizado. Únicamente se enviarán titulaciones de cursos que se hayan finalizado y completado
con éxito.</p>
<p>Acreditación: En el caso de que opte por matricularse en cursos a titulación propio de AYUDA T
LEARNING, el certificado que se emite por haber superado el curso, tendrá validez curricular.</p>
<p>Cuando se haya procedido a gestionar la matriculación por e mail, el siguiente paso será entregarle
una copia al alumno/empresa vía correo electrónico, con el fin de que la revise e identifique los
posibles errores. Si no remite la hoja de matriculación debidamente firmada, el alumno/a, al realizar y
formalizar el pago, manifiesta que conoce y acepta todas y cada una de las condiciones del curso/s en
que se matricula.</p>
	<h3>DURACIÓN</h3>
	<p>El máster tiene una duración de 9 meses teórico-prácticos (3 meses para cada área, laboral, contable
y fiscal) y 3 meses de prácticas.</p>
	<h3>PRORROGA</h3>
	<p>Cualquier alumno podrá solicitar una prorroga de la formación, que deberá ser motivada a través de un
modelo por escrito facilitado por AyudaTLearning. Esta prorroga se hará efectiva en cuanto a la
convocatoria siguiente que el alumno decida retomar. El plazo máximo de prorroga son 3 meses desde
su aceptación. En caso de necesitar una prorroga superior, se decidirá en función de las plazas
disponibles y demás circunstancias que supongan ese aplazamiento. En ningún caso, se hará el
aplazamiento de las cuotas atrasadas o que queden por pagar.</p>
	<h3>BOLSA DE EMPLEO AYUDA T PYMES</h3>
	<p>El alumno pertenecerá a la bolsa de empleo para asesores, despachos y asesorías de Ayuda T Pymes
del ámbito laboral, contable y fiscal.</p>
	<h3>FORMAS Y CONDICIONES DE PAGO</h3>
	<ul>
		<li>El pago se puede hacer en un único abono o en cuotas mensuales. Si desea optar por el pago completo,
se efectuará a través de transferencia bancaria o pago en línea.</li>
<li>Aunque se opte por un sistema de cuotas, una vez realizada la matriculación y comienzo del curso, se
deberá realizar el pago integro finalice la formación o no.</li>
<li>A continuación, se establecen las siguientes condiciones que resultan de aplicación, y a las cuales
se atendrá el alumno/empresa, para los distintos supuestos de pago:</li>
<li>Si el alumno/empresa reside en territorio español, los siguientes pagos se tienen que efectuar en la
fecha pactada por remesa bancaria, con independencia de cuál sea el medio de pago elegido para
llevar a cabo la formalización de la matrícula.</li>
<li>Si el alumno/empresa incumple alguna de los pagos, ello implica la pérdida de los derechos sobre el
curso, así como la pérdida para la obtención del título diploma acreditativo de la realización del
respectivo programa, así como la no devolución de los importes abonados y reclamación del importe
pendiente.</li>
<li>Única y exclusivamente se procederá a la devolución de las cantidades abonadas si AYUDA T
LEARNING decidiera no realizar la impartición de un programa.</li>
	</ul>
	<h3>DEVOLUCION DE PAGOS</h3>
	<p>En el supuesto de que el alumno decidiera rescindir el presente contrato antes de iniciar la formación,
podrán solicitar la anulación de la matrícula que hayan formalizado sin necesidad de justificación
alguna. Nunca se devolverá el importe de la matricula abonada, a efectos de reserva de plaza para la
formación en cuestión y la inclusión en un grupo formativo cerrado.</p>
<p>Los alumnos que lo deseen podrán solicitar la anulación de la matrícula que hayan formalizado, fuera
del plazo establecido en el apartado 1, en cualquier momento, sin que ello dé derecho a devolución de
las cuotas pagadas o exención de pago en el caso de que estuviesen pendientes (importe completo de
la formación contratada). En este caso habrá una penalización del pago del importe completo de la
formación contratada.</p>
<p>El alumno, al cumplimentar el formulario de matriculación, autoriza expresamente a AYUDA T
LEARNING para el Tratamiento de sus Datos personales, con el fin de poder remitirle comunicaciones
comerciales de los productos y servicios por parte de AYUDAT que resultando similares a los actuales,
habitualmente ofrecemos con unas condiciones más ventajosas, por cualquier medio, incluyéndose el
correo electrónico o similares.</p>
